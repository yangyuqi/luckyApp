public class InfoListData {
    private int count ;
    private List<InfoDetailsBean> tankList ;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public List<InfoDetailsBean> getTankList() {
        return tankList;
    }

    public void setTankList(List<InfoDetailsBean> tankList) {
        this.tankList = tankList;
    }

    public InfoListData(int count, List<InfoDetailsBean> tankList) {

        this.count = count;
        this.tankList = tankList;
    }
}
