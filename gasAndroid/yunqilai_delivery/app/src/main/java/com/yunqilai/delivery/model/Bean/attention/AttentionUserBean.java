public class AttentionUserBean {
    private String accessToken ;
    private String type ;
    private String attestationId ;
    private String name ;
    private String phone ;
    private String idCardNumber ;
    private String idCardFront ;
    private String idCardBack ;

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAttestationId() {
        return attestationId;
    }

    public void setAttestationId(String attestationId) {
        this.attestationId = attestationId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getIdCardNumber() {
        return idCardNumber;
    }

    public void setIdCardNumber(String idCardNumber) {
        this.idCardNumber = idCardNumber;
    }

    public String getIdCardFront() {
        return idCardFront;
    }

    public void setIdCardFront(String idCardFront) {
        this.idCardFront = idCardFront;
    }

    public String getIdCardBack() {
        return idCardBack;
    }

    public void setIdCardBack(String idCardBack) {
        this.idCardBack = idCardBack;
    }

    public AttentionUserBean(String accessToken, String type, String attestationId, String name, String phone, String idCardNumber, String idCardFront, String idCardBack) {

        this.accessToken = accessToken;
        this.type = type;
        this.attestationId = attestationId;
        this.name = name;
        this.phone = phone;
        this.idCardNumber = idCardNumber;
        this.idCardFront = idCardFront;
        this.idCardBack = idCardBack;
    }
}
