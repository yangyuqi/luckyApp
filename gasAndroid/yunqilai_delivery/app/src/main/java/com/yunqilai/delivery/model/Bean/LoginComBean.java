public class LoginComBean {
    private String icon ;
    private boolean needModifyPwd ;
    private String accessToken ;
    private String role ;
    private String userName ;
    private String refreshToken ;
    private String attestation ;
    private String mobile ;

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public boolean isNeedModifyPwd() {
        return needModifyPwd;
    }

    public void setNeedModifyPwd(boolean needModifyPwd) {
        this.needModifyPwd = needModifyPwd;
    }

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getRefreshToken() {
        return refreshToken;
    }

    public void setRefreshToken(String refreshToken) {
        this.refreshToken = refreshToken;
    }

    public String getAttestation() {
        return attestation;
    }

    public void setAttestation(String attestation) {
        this.attestation = attestation;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public LoginComBean(String icon, boolean needModifyPwd, String accessToken, String role, String userName, String refreshToken, String attestation, String mobile) {

        this.icon = icon;
        this.needModifyPwd = needModifyPwd;
        this.accessToken = accessToken;
        this.role = role;
        this.userName = userName;
        this.refreshToken = refreshToken;
        this.attestation = attestation;
        this.mobile = mobile;
    }
}
