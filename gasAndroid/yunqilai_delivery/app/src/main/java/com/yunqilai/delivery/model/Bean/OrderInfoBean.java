package com.yunqilai.delivery.model.Bean;

import java.util.List;
import java.util.Map;

/**
 * Created by yangyuqi on 17-7-26.
 */

public class OrderInfoBean {
    private int count ;
    private List<OrderDetailsBean> orders ;

    public OrderInfoBean(int count, List<OrderDetailsBean> orders) {
        this.count = count;
        this.orders = orders;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public List<OrderDetailsBean> getOrders() {
        return orders;
    }

    public void setOrders(List<OrderDetailsBean> orders) {
        this.orders = orders;
    }
}
