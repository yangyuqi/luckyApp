package com.yunqilai.delivery.model.Bean.my;

import java.util.List;

/**
 * Created by yangyuqi on 17-8-3.
 */

public class StaticData {
    private int total ;
    private List<StaticBean> weekList ;
    private int month ;
    private int today ;
    private List<StaticBean> monthList ;
    private int week ;

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public List<StaticBean> getWeekList() {
        return weekList;
    }

    public void setWeekList(List<StaticBean> weekList) {
        this.weekList = weekList;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getToday() {
        return today;
    }

    public void setToday(int today) {
        this.today = today;
    }

    public List<StaticBean> getMonthList() {
        return monthList;
    }

    public void setMonthList(List<StaticBean> monthList) {
        this.monthList = monthList;
    }

    public int getWeek() {
        return week;
    }

    public void setWeek(int week) {
        this.week = week;
    }

    public StaticData(int total, List<StaticBean> weekList, int month, int today, List<StaticBean> monthList, int week) {

        this.total = total;
        this.weekList = weekList;
        this.month = month;
        this.today = today;
        this.monthList = monthList;
        this.week = week;
    }
}
