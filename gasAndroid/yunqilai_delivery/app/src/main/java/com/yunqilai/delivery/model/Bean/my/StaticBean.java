public class StaticBean {
    private String time ;
    private String count ;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public StaticBean(String time, String count) {

        this.time = time;
        this.count = count;
    }
}
