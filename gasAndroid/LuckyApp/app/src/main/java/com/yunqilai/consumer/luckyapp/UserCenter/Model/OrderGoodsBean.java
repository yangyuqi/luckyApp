package com.yunqilai.consumer.luckyapp.UserCenter.Model;

import java.io.Serializable;

/**
 * Created by yangyuqi on 17-7-19.
 */

public class OrderGoodsBean implements Serializable{
    private String goodsId ;
    private String goodsName ;
    private String goodsModel ;
    private double goodsPrice ;
    private String goodsPhoto ;
    private int goodsCount ;
    private String shopCartId ;

    public String getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(String goodsId) {
        this.goodsId = goodsId;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public String getGoodsModel() {
        return goodsModel;
    }

    public void setGoodsModel(String goodsModel) {
        this.goodsModel = goodsModel;
    }

    public double getGoodsPrice() {
        return goodsPrice;
    }

    public void setGoodsPrice(double goodsPrice) {
        this.goodsPrice = goodsPrice;
    }

    public String getGoodsPhoto() {
        return goodsPhoto;
    }

    public void setGoodsPhoto(String goodsPhoto) {
        this.goodsPhoto = goodsPhoto;
    }

    public int getGoodsCount() {
        return goodsCount;
    }

    public void setGoodsCount(int goodsCount) {
        this.goodsCount = goodsCount;
    }

    public String getShopCartId() {
        return shopCartId;
    }

    public void setShopCartId(String shopCartId) {
        this.shopCartId = shopCartId;
    }

    public OrderGoodsBean(String goodsId, String goodsName, String goodsModel, double goodsPrice, String goodsPhoto, int goodsCount , String shopCartId) {

        this.goodsId = goodsId;
        this.goodsName = goodsName;
        this.goodsModel = goodsModel;
        this.goodsPrice = goodsPrice;
        this.goodsPhoto = goodsPhoto;
        this.goodsCount = goodsCount;
        this.shopCartId = shopCartId;

    }
}
