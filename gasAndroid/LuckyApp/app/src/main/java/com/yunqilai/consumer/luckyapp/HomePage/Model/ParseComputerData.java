public class ParseComputerData {
    private double totalGoodsPrice ;
    private double shipPrice ;
    private double upFloorPrice ;
    private double totalPrice ;

    public ParseComputerData(double totalGoodsPrice, double shipPrice, double upFloorPrice, double totalPrice) {
        this.totalGoodsPrice = totalGoodsPrice;
        this.shipPrice = shipPrice;
        this.upFloorPrice = upFloorPrice;
        this.totalPrice = totalPrice;
    }

    public double getTotalGoodsPrice() {
        return totalGoodsPrice;
    }

    public void setTotalGoodsPrice(double totalGoodsPrice) {
        this.totalGoodsPrice = totalGoodsPrice;
    }

    public double getShipPrice() {
        return shipPrice;
    }

    public void setShipPrice(double shipPrice) {
        this.shipPrice = shipPrice;
    }

    public double getUpFloorPrice() {
        return upFloorPrice;
    }

    public void setUpFloorPrice(double upFloorPrice) {
        this.upFloorPrice = upFloorPrice;
    }

    public double getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(double totalPrice) {
        this.totalPrice = totalPrice;
    }
}
