public class ParseOrderBean {
    private String code ;
    private String msg ;
    private ParseOrderDataBean data ;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public ParseOrderDataBean getData() {
        return data;
    }

    public void setData(ParseOrderDataBean data) {
        this.data = data;
    }

    public ParseOrderBean(String code, String msg, ParseOrderDataBean data) {

        this.code = code;
        this.msg = msg;
        this.data = data;
    }
}
