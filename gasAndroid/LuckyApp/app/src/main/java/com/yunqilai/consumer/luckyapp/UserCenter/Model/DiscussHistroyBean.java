public class DiscussHistroyBean {
    private String icon ;
    private String name ;
    private String date ;
    private String content ;

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public DiscussHistroyBean(String icon, String name, String date, String content) {

        this.icon = icon;
        this.name = name;
        this.date = date;
        this.content = content;
    }
}
