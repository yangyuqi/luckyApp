public class ThreeChannelBean {
    private String code ;
    private String msg ;

    public ThreeChannelBean(String code, String msg, Map<String, List<ThreeChannelInfoBean>> data) {
        this.code = code;
        this.msg = msg;
        this.data = data;
    }

    private Map<String,List<ThreeChannelInfoBean>> data ;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Map<String, List<ThreeChannelInfoBean>> getData() {
        return data;
    }

    public void setData(Map<String, List<ThreeChannelInfoBean>> data) {
        this.data = data;
    }
}
