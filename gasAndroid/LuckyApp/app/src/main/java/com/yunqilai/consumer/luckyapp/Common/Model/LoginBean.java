public class LoginBean {
    private String code ;
    private String msg ;
    private ParseLoginEntity data ;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public ParseLoginEntity getData() {
        return data;
    }

    public void setData(ParseLoginEntity data) {
        this.data = data;
    }

    public LoginBean(String code, String msg, ParseLoginEntity data) {

        this.code = code;
        this.msg = msg;
        this.data = data;
    }
}
