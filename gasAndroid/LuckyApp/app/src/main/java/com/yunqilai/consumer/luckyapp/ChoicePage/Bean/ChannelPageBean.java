package com.yunqilai.consumer.luckyapp.ChoicePage.Bean;

import java.util.List;
import java.util.Map;

/**
 * Created by yangyuqi on 17-8-4.
 */

public class ChannelPageBean {
    private String code ;
    private String msg ;
    private Map<String ,  List<ChoiceListBean>> data ;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Map<String, List<ChoiceListBean>> getData() {
        return data;
    }

    public void setData(Map<String, List<ChoiceListBean>> data) {
        this.data = data;
    }

    public ChannelPageBean(String code, String msg, Map<String, List<ChoiceListBean>> data) {

        this.code = code;
        this.msg = msg;
        this.data = data;
    }
}
