public class DetailsGoodsDataBean {

    private String goodsId ;
    private String goodsName ;
    private Double goodsPrice ;
    private String details ;
    private String goodsPhoto ;
    private List<String> photoList ;
    private List<SpecGoodsBean> specList ;
    private String goodsCount ;

    public String getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(String goodsId) {
        this.goodsId = goodsId;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public Double getGoodsPrice() {
        return goodsPrice;
    }

    public void setGoodsPrice(Double goodsPrice) {
        this.goodsPrice = goodsPrice;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public String getGoodsPhoto() {
        return goodsPhoto;
    }

    public void setGoodsPhoto(String goodsPhoto) {
        this.goodsPhoto = goodsPhoto;
    }

    public List<String> getPhotoList() {
        return photoList;
    }

    public void setPhotoList(List<String> photoList) {
        this.photoList = photoList;
    }

    public List<SpecGoodsBean> getSpecList() {
        return specList;
    }

    public void setSpecList(List<SpecGoodsBean> specList) {
        this.specList = specList;
    }

    public String getGoodsCount() {
        return goodsCount;
    }

    public void setGoodsCount(String goodsCount) {
        this.goodsCount = goodsCount;
    }

    public DetailsGoodsDataBean(String goodsId, String goodsName, Double goodsPrice, String details, String goodsPhoto, List<String> photoList, List<SpecGoodsBean> specList , String goodsCount) {

        this.goodsId = goodsId;
        this.goodsName = goodsName;
        this.goodsPrice = goodsPrice;
        this.details = details;
        this.goodsPhoto = goodsPhoto;
        this.photoList = photoList;
        this.specList = specList;
        this.goodsCount = goodsCount ;

    }
}
