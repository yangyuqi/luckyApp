package com.yunqilai.consumer.luckyapp.ChoicePage.Bean;

import java.util.List;

/**
 * Created by yangyuqi on 17-8-4.
 */

public class ChoiceBean {
    private String id ;
    private String title ;
    private List<ChoiceListBean> goodsList ;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public List<ChoiceListBean> getGoodsList() {
        return goodsList;
    }

    public void setGoodsList(List<ChoiceListBean> goodsList) {
        this.goodsList = goodsList;
    }

    public ChoiceBean(String id, String title, List<ChoiceListBean> goodsList) {

        this.id = id;
        this.title = title;
        this.goodsList = goodsList;
    }
}
