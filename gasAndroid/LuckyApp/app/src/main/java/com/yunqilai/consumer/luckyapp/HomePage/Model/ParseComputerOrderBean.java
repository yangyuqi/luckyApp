public class ParseComputerOrderBean {
    private String code ;
    private String msg ;
    private ParseComputerData data ;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public ParseComputerData getData() {
        return data;
    }

    public void setData(ParseComputerData data) {
        this.data = data;
    }

    public ParseComputerOrderBean(String code, String msg, ParseComputerData data) {

        this.code = code;
        this.msg = msg;
        this.data = data;
    }
}
